package tutorial4_2;

//증감 연산자 이해하기
public class Main {

	public static void main(String[] args) {

		int a = 10;
		System.out.println("현재의 a는 " + a + "입니다");
		a++;
		System.out.println("현재의 a는 " + a + "입니다");
		System.out.println("현재의 a는 "+ ++a +"입니다");
		System.out.println("현재의 a는 "+ a++ +"입니다");
		System.out.println("현재의 a는 " + a + "입니다");


	}

}
